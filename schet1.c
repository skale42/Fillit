#include "fillit.h"

int		schet1(char *str)
{
	int	j;
	int	i;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '.')
			str[i] = '0';
		if (str[i] == '#')
			str[i] = '1';
		if (str[i] == '\n' && (str[i + 1] == '\n' || str[i + 1] == '\0'))
			j++;
		i++;
	}
	return (j);
}
