#include "../libft.h"

char	*ft_strdup(const char *src)
{
	int		size;
	int		i;
	char	*new;

	size = ft_strlen(src);
	new = malloc(sizeof(char) * (size + 1));
	if (new == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
